package com.sp.exception;

public class FunctionnnalException extends Exception {
	
    public FunctionnnalException() {
        super();
    }

    public FunctionnnalException(String message) {
        super(message);
    }

    public FunctionnnalException(String message, Throwable cause) {
        super(message, cause);
    }

    public FunctionnnalException(Throwable cause) {
        super(cause);
    }	
}